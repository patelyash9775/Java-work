package lecture2;

public class BitwiseOp {

	public static void main(String[] args) {
		int num1=30,num2=6;
		System.out.println(" num1 & num2 = " + (num1 & num2));
		System.out.println(" num1 & num2 = " + (num1 | num2));
		System.out.println(" num1 & num2 = " + (num1 ^ num2));
		System.out.println(" ~num1= " + ~num1);
		
	}

}
